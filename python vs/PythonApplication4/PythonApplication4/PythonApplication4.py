a = True
b = False 
print(a)
print(b)
c = "True"
print(c)
print(type(a))         # tirnak icine almadan yazilmissa BOOL tipindedir 
print(type(c))         # tirnak icine alinip yazilmissa string olarak alinir 

yas1 = 20 
yas2 = 18 
print(yas1 > 18)
print(yas2 > 18)       # bool bu tarz sorgulamalar da kullanilir 

print(yas2 == 18)      # esit midir diye soruyoruz 
print(yas2 != 18)      # esit degil diye sorgulama yapiyoruz
print(not yas2 > 18)   # yas2 > 18 ifadesi false bir ifadedir ama basinda not (olumsuzluk) kullandigimiz icin true degerini dondurur 





